#TUPLE CONCATENTION
tuple1 = (1, 2, 3)
tuple2 = (4, 5)
combined = tuple1 + tuple2
print(combined)  # Output: (1, 2, 3, 4, 5)


#TUPLE INDEX
my_tuple = (10, 20, 30)
print(my_tuple[1])  # Output: 20

#TUPLE LENGTH
my_tuple = (10, 20, 30)
print(len(my_tuple))  # Output: 3


#TUPLE MEMBERSHIP
my_tuple = (10, 20, 30)
print(len(my_tuple))  # Output: 3


#TUPLE REPETITION
my_tuple = (1, 2)
repeated = my_tuple * 3
print(repeated) 
# Output: (1, 2, 1, 2, 1, 2)


#TUPLE SLICE
my_tuple = (10, 20, 30, 40, 50)
print(my_tuple[1:4])  # Output: (20, 30, 40)





